package mvc;

public class registro {
    private String placa,nombre_invitado,apto;
    private int puesto;

    public String getPlaca() { return placa; }
    public void setPlaca(String placa) { this.placa = placa; }
    public String getNombre_invitado() { return nombre_invitado; }
    public void setNombre_invitado(String nombre_invitado) { this.nombre_invitado = nombre_invitado; }
    public String getApto() { return apto; }
    public void setApto(String apto) { this.apto = apto;  }
    public int getPuesto() { return puesto; }
    public void setPuesto(int puesto) { this.puesto = puesto; }
}
